/**
 *
 * @param {Array[]} arr
 */
var sortArrayWithoutZero = function (arr) {
  let NotzerArr = [];
  let zerArr = [];
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] != 0) {
      NotzerArr.push(arr[i]);
    } else {
      zerArr.push(arr[i]);
    }
  }
  return [...NotzerArr, ...zerArr];
};
